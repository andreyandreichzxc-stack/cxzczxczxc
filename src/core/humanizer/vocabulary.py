def get_ai_markers(user_slots: list | None = None) -> dict[str, float]:
    """Собирает AI-маркеры: базовые + из моделей пользователя.

    Args:
        user_slots: Список LlmKeySlot объектов пользователя (опционально).
                   Если None — только базовые маркеры.

    Returns:
        Словарь {фраза: вес}. Базовые маркеры сохраняют оригинальные веса,
        модельные маркеры получают вес 0.65.
    """
    if not user_slots:
        return BASE_AI_MARKERS  # read-only, копия не нужна

    markers = dict(BASE_AI_MARKERS)

    if user_slots:
        from src.llm.provider_catalog import get_provider  # lazy import, no cycles

        for slot in user_slots:
            if not slot.enabled or slot.category != "llm":
                continue
            pi = get_provider(slot.provider)
            if pi and pi.ai_markers:
                for marker in pi.ai_markers:
                    if marker not in markers:
                        markers[marker] = 0.65  # вес для модельных маркеров
    return markers
