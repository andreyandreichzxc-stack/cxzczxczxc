"""Команда /help — справочник по командам бота с группировкой по категориям."""

from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

from src.bot.filters import OwnerOnly

router = Router(name="help")
router.message.filter(OwnerOnly())

HELP_TEXT = """\
<b>📋 Команды бота</b>

<b>👤 Люди</b>
/contact Имя — что я знаю о человеке
/timeline тема — хронология обсуждений
/send Имя текст — написать человеку
/style Имя — мой стиль общения с человеком

<b>🧠 Память и задачи</b>
/todos — список обещаний
/remember факт — запомнить факт
/forget запрос — забыть факты

<b>🔍 Поиск</b>
/search текст — поиск в чатах
/index Имя — проиндексировать чат

<b>🌀 Inline-режим</b>
@botname запрос — поиск в памяти, контексте и переписках из любого чата

<b>📰 Новости и дайджест</b>
/news тема — дайджест каналов
/news_channels — источники новостей
/digest — утренний дайджест

<b>🔄 Аккаунт</b>
/login — авторизоваться
/logout — выйти
/sync — обновить контакты

<b>🔑 Ключи и API</b>
/keys — слоты ключей
/keys add — добавить ключ
/keys import — импорт списком
/gates — проверка зависимостей
/docs endpoints — инструкция по кастомным API

<b>⚙️ Настройки</b>
/settings — всё через меню
/humanize текст — анализ AI-шаблонности
📤📥 Экспорт/Импорт конфига — сохрани свои настройки в файл и восстанови на новом сервере

<b>✏️ Обычный язык</b>
Просто напиши или скажи голосом — я пойму и сделаю.

🔧 <b>Новые возможности</b>

<b>⚙️ Настройки AI</b>
/settings → 🧠 LLM и модели → Возможности AI:
  • Embedding — поиск по смыслам в памяти
  • Vision — анализ фото (отправь фото боту)
  • STT/Audio — расшифровка голосовых
  • TTS — синтез речи
  • Авто-выбор модели — умный подбор LLM под задачу

<b>🖼 Анализ фото</b>
Отправь боту фото — он опишет что на нём.
Повторная отправка того же фото — мгновенный ответ из кэша.
В ответе показывается статистика потраченных токенов.

<b>🔍 Inline-режим</b>
@botname запрос — поиск в памяти, контексте и переписках из любого чата.
Пример: @botname встреча с Машей → найдет все факты о встречах.

<b>🤖 Агенты</b>
Maestro автоматически выбирает специалиста под задачу:
  • researcher — поиск и анализ
  • coder — написание кода
  • creative — идеи и креатив
  • analyst — анализ данных
  • random — нестандартные задачи

<b>🎛️ Кастомизация</b>
.env файл поддерживает настройки:
  • streaming_cursor — курсор при стриминге (🦊)
  • streaming_edit_interval — скорость обновления (0.3 сек)
  • streaming_enabled — вкл/выкл стриминг
  • photo_cache_ttl_sec — кэш фото (секунд)
  • vision_enabled — вкл/выкл анализ фото
  • embedding_enabled — вкл/выкл эмбеддинги
  • audio_enabled — вкл/выкл распознавание речи
  • auto_select_model — вкл/выкл авто-выбор модели

<i>⚡ Некоторые фичи требуют API-ключей (OpenAI/Gemini).
Добавь ключ через /settings → 🔑 API-ключи.</i>
"""


@router.message(Command("help"))
async def cmd_help(message: Message) -> None:
    """Показать справочник команд, сгруппированный по категориям."""
    await message.answer(HELP_TEXT)
